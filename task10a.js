// This program demonstrates how to store a person's name in a variable
// and print the name in lowercase, uppercase, and titlecase.

let personName = "Gauhar";

// Print in lowercase
console.log(personName.toLowerCase());

// Print in uppercase
console.log(personName.toUpperCase());

// Print in titlecase
console.log(personName.charAt(0).toUpperCase() + personName.slice(1).toLowerCase());
